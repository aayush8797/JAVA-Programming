import java.util.*;

public class binary_operators {
    public static void main(String[] args) {
        try(Scanner sc= new Scanner(System.in)){
            int a = sc.nextInt();
            int b = sc.nextInt();
            System.out.println(a+b);
            System.out.println(a-b);
            System.out.println(a*b);
            System.out.println(a/b);
            System.out.println(a%b);

        }
    }
    
}
