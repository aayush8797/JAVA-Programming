public class relational_operator {
    public static void main(String[] args) {
        //equal to equal to operator
        int a =10;
        int b =15;
        System.out.println(a==b);

        //not equal to
        System.out.println(a!=b);

        //greater than
        System.out.println(a>b);

        //lesser than
        System.out.println(a<b);

        //greater than equal to
        System.out.println(a>=b);

        //lesser than equal to
        System.out.println(a<=b);

    }
}
